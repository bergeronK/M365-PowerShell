	[void][reflection.assembly]::LoadWithPartialName("System.Windows.Forms")
	[System.Windows.Forms.Application]::EnableVisualStyles()
	$formMRSExplorer = New-Object 'System.Windows.Forms.Form'
	$tabContainer = New-Object 'System.Windows.Forms.TabControl'
	$tabXml = New-Object 'System.Windows.Forms.TabPage'
	$splitVertical = New-Object 'System.Windows.Forms.SplitContainer'
	$textNodePath = New-Object 'System.Windows.Forms.TextBox'
	$buttonImport = New-Object 'System.Windows.Forms.Button'
	$textImportFile = New-Object 'System.Windows.Forms.TextBox'
	$tabQuery = New-Object 'System.Windows.Forms.TabPage'
	$buttonReset = New-Object 'System.Windows.Forms.Button'
	$checkboxSavePreferences = New-Object 'System.Windows.Forms.CheckBox'
	$buttonConnect = New-Object 'System.Windows.Forms.Button'
	$buttonSearch = New-Object 'System.Windows.Forms.Button'
	$textSearch = New-Object 'System.Windows.Forms.TextBox'
	$buttonExportXML = New-Object 'System.Windows.Forms.Button'
	$textConnectionUrl = New-Object 'System.Windows.Forms.TextBox'
	$comboConnection = New-Object 'System.Windows.Forms.ComboBox'
	$panelQueryStatus = New-Object 'System.Windows.Forms.Panel'
	$textQueryStatus = New-Object 'System.Windows.Forms.TextBox'
	$buttonBrowseXML = New-Object 'System.Windows.Forms.Button'
	$buttonRefresh = New-Object 'System.Windows.Forms.Button'
	$comboFilter = New-Object 'System.Windows.Forms.ComboBox'
	$dataMoveRequest = New-Object 'System.Windows.Forms.DataGridView'
	$labelSessionStatus = New-Object 'System.Windows.Forms.Label'
	$treeCollectionNodes = New-Object 'System.Windows.Forms.TreeView'
	$listCollectionEntries = New-Object 'System.Windows.Forms.ListBox'
	$fileOpenDialog = New-Object 'System.Windows.Forms.OpenFileDialog'
	$menuContext = New-Object 'System.Windows.Forms.ContextMenuStrip'
	$menuContextCopy = New-Object 'System.Windows.Forms.ToolStripMenuItem'
	$textEntryDetails = New-Object 'System.Windows.Forms.TextBox'
	$splitHorizontal = New-Object 'System.Windows.Forms.SplitContainer'
	$fileSaveDialog = New-Object 'System.Windows.Forms.SaveFileDialog'
	$InitialFormWindowState = New-Object 'System.Windows.Forms.FormWindowState'
	
	Function Write-Console ($LogString) {
	    $LogStatus = $LogString.Split(":")[0]
	    If ($LogStatus -eq "ALERT") { Write-Host "`n$LogString" -ForegroundColor Yellow }
	    If ($LogStatus -eq "INFO") { Write-Host "`n$LogString" -ForegroundColor Cyan }
	    If ($LogStatus -eq "ERROR") { Write-Host "`n$LogString" -BackgroundColor Red }
	    If ($LogStatus -eq "SUCCESS") { Write-Host "`n$LogString" -ForegroundColor Green }
	    If ($LogStatus -eq "DEBUG") { Write-Host "`n$LogString" -ForegroundColor Gray }
	    If ($LogStatus -eq "") { Write-Host "`n" }
	}
	
	Function Load-DataGridView ($DataGridView, $Item, $DataMember) {
		$DataGridView.SuspendLayout()
		$DataGridView.DataMember = $DataMember
		If ($Item -Is [System.ComponentModel.IListSource] -Or $Item -Is [System.ComponentModel.IBindingList] -Or $Item -Is [System.ComponentModel.IBindingListView] ) { $DataGridView.DataSource = $Item }
		Else {
			$Array = New-Object System.Collections.ArrayList
			If ($Item -is [System.Collections.IList]) { $Array.AddRange($Item) }
			Else { $Array.Add($Item) }
			$DataGridView.DataSource = $Array }
		$DataGridView.ResumeLayout()
	}
	
	Function Retrieve-MoveRequests {
        Write-Console "INFO: Retrieve move requests..."
		$panelQueryStatus.Show()
		$dataMoveRequest.DataSource = $null
		If ($textSearch.Text -ne "" -and $textSearch.Text -ne "Name, Alias, or Batch") {
			If ($comboFilter.Text -eq "All") { $MoveRequestList = (Get-MoveRequest -ResultSize Unlimited | Where { $_.Alias -like "*$($textSearch.Text)*" -or $_.Name -like "*$($textSearch.Text)*" -or $_.BatchName -like "*$($textSearch.Text)*" } | Select Name,Alias,BatchName,RemoteHostName,Flags,TargetDatabase,Status) | Sort Alias }
			Else { $MoveRequestList = (Get-MoveRequest -ResultSize Unlimited -MoveStatus "$($comboFilter.Text)" | Where { $_.Alias -like "*$($textSearch.Text)*" -or $_.Name -like "*$($textSearch.Text)*" -or $_.BatchName -like "*$($textSearch.Text)*" } | Select Name,Alias,BatchName,RemoteHostName,Flags,TargetDatabase,Status) | Sort Alias } }
		Else { 
			If ($comboFilter.Text -eq "All") { $MoveRequestList = (Get-MoveRequest -ResultSize Unlimited | Select Name,Alias,BatchName,RemoteHostName,Flags,TargetDatabase,Status) | Sort Alias }
			Else { $MoveRequestList = (Get-MoveRequest -ResultSize Unlimited -MoveStatus "$($comboFilter.Text)" | Select Name,Alias,BatchName,RemoteHostName,Flags,TargetDatabase,Status) | Sort Alias } }
		$panelQueryStatus.Hide()
		If ($MoveRequestList -eq $null) {
			Write-Console "ERROR: No move request results found."
			$buttonBrowseXML.Enabled = $false; $buttonExportXML.Enabled = $false }
		Else {
		    Load-DataGridView $dataMoveRequest $MoveRequestList
			$buttonBrowseXML.Enabled = $true; $buttonExportXML.Enabled = $true }
	}
	
	Function Retrieve-MoveRequestStatistics {
        Write-Console "INFO: Retrive move request report..."
		$panelQueryStatus.Show()
		$textEntryDetails.Clear(); $listCollectionEntries.Items.Clear(); $treeCollectionNodes.Nodes.Clear()
		$textImportFile.Text = $dataMoveRequest.CurrentRow.Cells["Alias"].Value
		$script:MoveRequest = (Get-MoveRequestStatistics $textImportFile.Text -IncludeReport)
		$panelQueryStatus.Hide()
		ForEach ($Collection in ($MoveRequest | Get-Member | Where { $_.MemberType -eq "Property" -and $_.Name -ne "Length" })) { $treeCollectionNodes.Nodes.Add($Collection.Name) }
	}
	
	$buttonImport_Click={
		$fileOpenDialog.Reset()
		$fileOpenDialog.Title = "Open Move Request XML Export File"
		$fileOpenDialog.ShowDialog()
		$textImportFile.Text = $fileOpenDialog.FileName
		$textEntryDetails.Clear(); $listCollectionEntries.Items.Clear(); $treeCollectionNodes.Nodes.Clear()
		$script:MoveRequest = Import-Clixml $fileOpenDialog.FileName
		ForEach ($Collection in ($MoveRequest | Get-Member | Where { $_.MemberType -eq "Property" -and $_.Name -ne "Length" })) { $treeCollectionNodes.Nodes.Add($Collection.Name) }
	}
	
	$treeCollectionNodes_AfterSelect=[System.Windows.Forms.TreeViewEventHandler]{
		$script:SelectedTreeNodePath = $treeCollectionNodes.SelectedNode.FullPath.Replace("\",".")
		$textNodePath.Text = $SelectedTreeNodePath
		$textEntryDetails.Clear(); $listCollectionEntries.Items.Clear()
		$Expression = '$MoveRequest' + ".$SelectedTreeNodePath"
		$Entries = Invoke-Expression $Expression
		If ($Entries.Count -eq $null) {
			[string]$Entry = Invoke-Expression $Expression
			$Entries = $Entry.Split(";").Trim() }
		Foreach ($Item in $Entries) { $listCollectionEntries.Items.Add($Item) }
		$SelectedTreeNode = $treeCollectionNodes.SelectedNode
		$SelectedTreeNode.Nodes.Clear()
		If ($textNodePath.Text -eq "Report") {
			$Expression = '$MoveRequest' + ".$SelectedTreeNodePath" + ' | Get-Member | Where { $_.MemberType -eq "Property" -and $_.Name -ne "Length" }'
			$Collection = Invoke-Expression $Expression
			If ($Collection -ne $null) { ForEach ($Item in $Collection) { $SelectedTreeNode.Nodes.Add($Item.Name) } } }
	}
	
	$listCollectionEntries_SelectedIndexChanged={
		$textEntryDetails.Clear()
		If ($listCollectionEntries.Items.Count -gt 1) { $textNodePath.Text = $SelectedTreeNodePath + "[" + $listCollectionEntries.SelectedIndex + "]" }
		$Expression = '$MoveRequest' + ".$($textNodePath.Text)" + ' | Out-String'
		$Entries = Invoke-Expression $Expression
		If ($Entries -ne $null) { ForEach ($Item in $Entries) { $textEntryDetails.Text = $Item } }
	}
	
	$menuContextCopy_Click={
		$listCollectionEntries.SelectedItem | Clip
	}
	
	$tabQuery_Enter={
		If ($MRS_ExplorerRemoteSession -eq $null -and $LocalSession -ne "Initialized") {
			$CheckLocalSession = (Get-PSSession)
				If ($CheckLocalSession.State -eq "Opened" -and $CheckLocalSession.ConfigurationName -eq "Microsoft.Exchange" -and $CheckLocalSession.Availability -eq "Available") {
					Write-Console "ALERT: Local session detected."; $script:LocalSession = "Initialized"
					$labelSessionStatus.Text = "Connected"; $labelSessionStatus.BackColor = 'Green'; $buttonConnect.Text = "Disconnect"
					$comboConnection.Enabled = $false; $textConnectionUrl.Text = "$($CheckLocalSession.ComputerName)"
                    Retrieve-MoveRequests } }
	}
	
	$comboConnection_SelectedIndexChanged={
		If ($comboConnection.Text -eq "Commercial Worldwide") { $textConnectionUrl.Text = "https://outlook.office365.com/powershell-liveid" }
		If ($comboConnection.Text -eq "Federal & Defense ITAR") { $textConnectionUrl.Text = "https://outlook.office365.us/powershell-liveid" }
		If ($comboConnection.Text -eq "On-Premises & Dedicated") { $textConnectionUrl.Text = "Enter a custom PowerShell endpoint..." }
	}
	
	$buttonConnect_Click={
		If ($buttonConnect.Text -eq "Connect") { $ConnectionStatus = "Disconnected" }
		If ($buttonConnect.Text -eq "Disconnect") { $ConnectionStatus = "Connected" }
		If ($ConnectionStatus -eq "Disconnected") {
            Write-Console "INFO: Connecting to remote PowerShell session..."
			$buttonConnect.Text = "Connecting..."; $labelSessionStatus.Text = ""; $labelSessionStatus.BackColor = 'Transparent'
			If ($comboConnection.Text -eq "On-Premises & Dedicated") {
				Write-Console "INFO: Trying Kerberos authentication..."
				$script:MRS_ExplorerRemoteSession = New-PSSession -ConfigurationName Microsoft.Exchange -ConnectionUri $textConnectionUrl.Text -Authentication Kerberos -AllowRedirection }
			If ($MRS_ExplorerRemoteSession -eq $null -and $Config.UserName -ne $null -and $Config.Password -ne $null) {
					Write-Console "INFO: Trying preference credentials..."
					$MRS_ExplorerRemoteCredentials = New-Object System.Management.Automation.PSCredentials($Config.UserName, $Config.Password)
					$script:MRS_ExplorerRemoteSession = New-PSSession -ConfigurationName Microsoft.Exchange -ConnectionUri $textConnectionUrl.Text -Authentication Basic -Credential $MRS_ExplorerRemoteCredentials -AllowRedirection }
			If ($MRS_ExplorerRemoteSession -eq $null) {
				Write-Console "ALERT: Prompting for credentials..."
				Try { $script:MRS_ExplorerRemoteCredentials = Get-Credential }
				Catch { Write-Console "ERROR: Credential prompt failed." }
				If ($MRS_ExplorerRemoteCredentials -ne $null) { $script:MRS_ExplorerRemoteSession = New-PSSession -ConfigurationName Microsoft.Exchange -ConnectionUri $textConnectionUrl.Text -Authentication Basic -Credential $MRS_ExplorerRemoteCredentials -AllowRedirection } }
			If ($MRS_ExplorerRemoteSession -ne $null) {
				Write-Console "SUCCESS: Remote session connected."
				Import-Module (Import-PSSession $MRS_ExplorerRemoteSession -DisableNameChecking -AllowClobber) -Global -DisableNameChecking
				$labelSessionStatus.Text = "Connnected"; $labelSessionStatus.BackColor = 'Green'; $buttonConnect.Text = "Disconnect"
				Retrieve-MoveRequests }
			If ($MRS_ExplorerRemoteSession -eq $null) {
				Write-Console "ERROR: Remote connection failed."
				$buttonConnect.Text = "Connect"; $labelSessionStatus.Text = "Offline"; $labelSessionStatus.BackColor = 'Red'
				$MRS_ExplorerRemoteCredentials = $null; $MRS_ExplorerRemoteSession = $null } }
		If ($ConnectionStatus -eq "Connected") {
			$dataMoveRequest.DataSource = $null; $textSearch.Text = ""; $comboFilter = "All"
			Get-PSSession | Remove-PSSession
            $script:MRS_ExplorerRemoteSession = $null; $script:MRS_ExplorerRemoteCredentials = $null 
			$comboConnection.Enabled = $true; $labelSessionStatus.Text = "Offline"; $labelSessionStatus.BackColor = "Red"; $buttonConnect.Text = "Connect"
			Write-Console "SUCCESS: Remote session disconnected." }
	}
	
	$textConnectionUrl_Enter={
		If ($comboConnection.Text -eq "On-Premises & Dedicated" -and $textConnectionUrl.Text -eq "Enter a custom Powershell endpoint...") { $textConnectionUrl.Text = "" }
	}
	
	$checkboxSavePreferences_CheckedChanged={
		If ($checkboxSavePreferences.Checked) { $script:SaveConfig = $true }
	}
	
	$buttonBrowseXML_Click={
		Retrieve-MoveRequestStatistics
		$tabContainer.SelectedTab = $tabXml
	}
	
	$dataMoveRequest_DoubleClick={
		Retrieve-MoveRequestStatistics
		$tabContainer.SelectedTab = $tabXml
	}
	
	$buttonExportXML_Click={
		$fileSaveDialog.Reset()
		$fileSaveDialog.Title = "Save Move Request XML Export File"
		$fileSaveDialog.FileName = "$($dataMoveRequest.CurrentRow.Cells["Alias"].Value).xml"
		$fileSaveDialog.ShowDialog()
		Retrieve-MoveRequestStatistics
		$MoveRequest | Export-Clixml $fileSaveDialog.FileName
	}
	
	$comboFilter_SelectedIndexChanged={
        Retrieve-MoveRequests
	}
	
	$buttonRefresh_Click={
        Retrieve-MoveRequests
	}
	
	$textSearch_Enter={
		If ($textSearch.Text -eq "Name, Alias, or Batch") { $textSearch.Text = "" }
	}
	
	$buttonSearch_Click={
        Retrieve-MoveRequests
	}
	
	$buttonReset_Click={
		$textSearch.Text = "Name, Alias, or Batch"
        If ($comboFilter.Text -eq "All") { &$comboFilter_SelectedIndexChanged }
        Else { $comboFilter.Text = "All" }
	}
	
	$Form_StateCorrection_Load={
		$formMRSExplorer.WindowState = $InitialFormWindowState
	}
	
	$Form_Cleanup_FormClosed={
		Try {
			$buttonImport.remove_Click($buttonImport_Click)
			$buttonReset.remove_Click($buttonReset_Click)
			$checkboxSavePreferences.remove_CheckedChanged($checkboxSavePreferences_CheckedChanged)
			$buttonConnect.remove_Click($buttonConnect_Click)
			$buttonSearch.remove_Click($buttonSearch_Click)
			$textSearch.remove_Enter($textSearch_Enter)
			$buttonExportXML.remove_Click($buttonExportXML_Click)
			$textConnectionUrl.remove_Enter($textConnectionUrl_Enter)
			$comboConnection.remove_SelectedIndexChanged($comboConnection_SelectedIndexChanged)
			$buttonBrowseXML.remove_Click($buttonBrowseXML_Click)
			$buttonRefresh.remove_Click($buttonRefresh_Click)
			$comboFilter.remove_SelectedIndexChanged($comboFilter_SelectedIndexChanged)
			$dataMoveRequest.remove_DoubleClick($dataMoveRequest_DoubleClick)
			$tabQuery.remove_Enter($tabQuery_Enter)
			$treeCollectionNodes.remove_AfterSelect($treeCollectionNodes_AfterSelect)
			$listCollectionEntries.remove_SelectedIndexChanged($listCollectionEntries_SelectedIndexChanged)
			$menuContextCopy.remove_Click($menuContextCopy_Click)
			$formMRSExplorer.remove_Load($Form_StateCorrection_Load)
			$formMRSExplorer.remove_FormClosed($Form_Cleanup_FormClosed)
		}
		Catch [Exception]{ }
	}

