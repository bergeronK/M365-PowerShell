Function OnApplicationLoad {
	If (Test-Path .\MRS_Explorer.cfg) {
		Write-Console "INFO: Preference file detected."
		$script:Config = Import-Csv ".\MRS_Explorer.cfg" }
	Return $true
}

Function OnApplicationExit {
	If ($SaveConfig -eq $true) {
		$ConfigConnection = $comboConnection.Text
		$ConfigConnectionUrl = $textConnectionUrl.Text
		$ConfigUserName = $MRS_ExplorerRemoteCredentials.UserName
		$ConfigPassword = $MRS_ExplorerRemoteCredentials.Password | ConvertFrom-SecureString
		"Connection,ConnectionUrl,UserName,Password" | Out-File "c:\Scripts\MRS_Explorer.cfg"
		"$ConfigConnection,$ConfigConnectionUrl,$ConfigUserName,$ConfigPassword" | Out-File "c:\Scripts\MRS_Explorer.cfg" -Append }
	If ($MRS_ExplorerRemoteSession -ne $null) {
		Write-Console "INFO: Disconnect remote session."
		Remove-PSSession $MRS_ExplorerRemoteSession }
	$script:ExitCode = 0
}

